    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double[][] ecuaciones = new double[restricciones][3];
        for(int i = 0; i < restricciones; i++){
            ecuaciones[i][0] = Double.parseDouble(spnx1[i].getValue().toString());
            ecuaciones[i][1] = Double.parseDouble(spnx2[i].getValue().toString());
            ecuaciones[i][2] = Double.parseDouble(spnsol[i].getValue().toString());
        }
        
        double[] x1 = new double[restricciones];
        double[] x2 = new double[restricciones];
        double[] sol = new double[restricciones];
        for(int i = 0; i < restricciones; i++){
            x1[i] = Double.parseDouble(spnx1[i].getValue().toString());
            x2[i] = Double.parseDouble(spnx2[i].getValue().toString());
            sol[i] = Double.parseDouble(spnsol[i].getValue().toString());
        }
        
        Restricciones res = new Restricciones(x1, x2, sol);
        double[][] puntos = new double[restricciones][2];
        String[] nombres = new String[restricciones];
        for(int i = 0; i < restricciones; i++){
            puntos[i] = res.obtenerPuntos(x1[i], x2[i], sol[i]);
            nombres[i] = "R" + (i+1);
        }
        
        DefaultCategoryDataset dataset  = new DefaultCategoryDataset();
        double maximo = obtenerValorMaximo(ecuaciones);
        
        for(int i = 0; i < restricciones; i++){
            dataset.addValue(puntos[i][0], nombres[i], "" + i);
            dataset.addValue(i, nombres[i], "" + puntos[i][1]);
        }
        
        JFreeChart grafica = ChartFactory.createLineChart(
                "Metodo Grafico",               // Titulo del grafico
                "X",                            // Etiqueta del eje horizontal
                "Y",                            // Etiqueta del eje vertical
                dataset,                        // Modelo de datos
                PlotOrientation.VERTICAL,       // Vertical u Horizontal
                true,                           // Etiquetas de los colores del grafico
                true,                           // Etiqueta en raton
                false                           // Agregar URL
        );
        
        ChartPanel Panel = new ChartPanel(grafica);
        JFrame Ventana = new JFrame("");
        Ventana.getContentPane().add(Panel);
        Ventana.pack();
        Ventana.setVisible(true);
        Ventana.setDefaultCloseOperation(JFrame.HIDE_ON_CLOSE);
        
        double[] resultado = res.despejarEcuaciones(ecuaciones[0], ecuaciones[1]);
        jLabel9.setText(resultado[0] + "");
        jLabel10.setText(resultado[1] + "");
        double z = (Double.parseDouble(jSpinner1.getValue().toString()) * resultado[0]) + (Double.parseDouble(jSpinner2.getValue().toString()) * resultado[1]);
        jLabel11.setText(z + "");
    }//GEN-LAST:event_jButton3ActionPerformed

