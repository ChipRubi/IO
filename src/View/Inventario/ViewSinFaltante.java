    private void getDataView() {
        demanda = Double.parseDouble(jSpinner1.getValue().toString());
        costoPedido = Double.parseDouble(jSpinner2.getValue().toString());
        costoUnitario = Double.parseDouble(jSpinner3.getValue().toString());
        costoMantenimiento = Double.parseDouble(jSpinner4.getValue().toString());
    }

    private void setDataView() {
        SinFaltante sinFaltante = new SinFaltante(demanda, costoPedido, costoUnitario, costoMantenimiento);
        double costoTotal = sinFaltante.calcularCostoTotal();
        double unidadesPedido = sinFaltante.calcularUnidadesPedido();
        double numeroPedidos = sinFaltante.calcularNumeroPedidos();
        double tiempoPedido = sinFaltante.calcularTiempoPedidos();
        
        jLabel10.setText("$ " + String.format("%.2f", costoTotal));
        jLabel11.setText(String.format("%.2f", unidadesPedido) + " unidades");
        jLabel12.setText(String.format("%.2f", numeroPedidos) + " pedidos anuales");
        jLabel13.setText(String.format("%.2f", tiempoPedido) + " a√±os");
    }
