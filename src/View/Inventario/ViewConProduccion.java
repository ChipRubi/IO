    private void getDataView() {
        demanda = Double.parseDouble(jSpinner1.getValue().toString());
        demandaMinima = Double.parseDouble(jSpinner2.getValue().toString());
        produccion = Double.parseDouble(jSpinner3.getValue().toString());
        costoPedido = Double.parseDouble(jSpinner5.getValue().toString());
        costoAlmacenamiento = Double.parseDouble(jSpinner6.getValue().toString());
    }

    private void setDataView() {
        ConProduccion conProduccion;
        if(demandaMinima == 0){
            conProduccion = new ConProduccion(demanda, produccion, costoPedido, costoAlmacenamiento);
        } else {
            conProduccion = new ConProduccion(demanda, demandaMinima, produccion, costoPedido, costoAlmacenamiento);
        }
        
        double costoTotal = conProduccion.calcularCostoTotal();
        double unidadesPedido = conProduccion.calcularUnidadesPedido();
        double numeroPedidos = conProduccion.calcularNumeroPedidos();
        double tiempoPedidos = conProduccion.calcularTiempoPedidos();
        
        jLabel12.setText("$ " + String.format("%.2f", costoTotal));
        jLabel13.setText(String.format("%.2f", unidadesPedido) + " unidades");
        jLabel14.setText(String.format("%.2f", numeroPedidos) + " pedidos por a침o");
        jLabel15.setText(String.format("%.2f", tiempoPedidos) + " a침os");
    }
